public class Objective7Lab4 {

  public static void main(String[] args) {

    int counter = 1;
    int sum = 1;


    while (counter <= 20) {
      sum = sum + counter;
      counter = counter + 1;
      }
//put the println on the outside of the curly braces for sum without counter
      System.out.println(sum);

  }
}
